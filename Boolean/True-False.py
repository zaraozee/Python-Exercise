x = True
y = False

print("x AND y:", x and y)   # False
print("x OR y:", x or y)     # True
print("NOT x:", not x)       # False
print("NOT y:", not y)       # True

a = 10
b = 20

print("a lebih besar dari b:", a > b)      # False
print("a lebih kecil dari b:", a < b)      # True
print("a sama dengan b:", a == b)          # False
print("a tidak sama dengan b:", a != b)    # True
